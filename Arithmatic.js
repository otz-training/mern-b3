let num1 = 30;
let num2 = 20;

console.log(num1 + num2); //50
console.log(num1 - num2); //10
console.log(num1 * num2); //600
console.log(num1 / num2); //1.5
console.log(num1 % num2); //10

console.log(num1 + " + " + num2 + "=" + (num1 + num2));
console.log(num1 + " - " + num2 + "=" + (num1 - num2));
console.log(num1 + " * " + num2 + "=" + num1 * num2);
console.log(num1 + " / " + num2 + "=" + num1 / num2);
console.log(num1 + " % " + num2 + "=" + (num1 % num2));

console.log(`${num1} + ${num2}  = ${num1 + num2}`);
